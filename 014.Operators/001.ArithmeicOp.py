# Operators in Python - These are used to perform operations on variables and values.

# Arithmetic Operators - These are used to perform mathematical operations. 
# Addition - + +> 5 + 3 = 8 
print("Addition:",5+3)

# Subtraction - - > 5 - 3 = 2
print("Subtraction :", 5-3)

# Multiplication - * > 5 * 3 = 15
print("Multiplication:", 5*3)
# Division - / > 5 / 3 = 1.6666666666666667
print("Division (Complete) :", 16/3)
# Floor (Integer) Division - // > 5 // 3 = 1 
print("Floor division :",16//3) 
# Exponentiation - ** > 5 ** 3 = 125  -- Math Module -- Pow()
print("Exponentiation :", 5**3)
# Modulus (Remainder) - % > 5 % 3 = 2
print("Modulus (Remainder) Operator: ",17%3)




# Comparision Operators : 
# 1. Equal to : a == b
print(3==3)
print(5==4)
# 2. Not Equal to : a != b  # a is not equal to b
print(3!=3)
print(5!=4)
# 3. Smalller than : a < b  # a is smaller than b
print(3<5)
print(5<4)
# 4. Greater than : a > b  # a is greater than b
print(3>5)
print(5>4)

